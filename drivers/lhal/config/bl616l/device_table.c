struct bflb_device_s *bflb_device_get_by_name(const char *name)
{
    for (uint8_t i = 0; i < sizeof(bl616l_device_table) / sizeof(bl616l_device_table[0]); i++) {
        if (arch_strcmp(bl616l_device_table[i].name, name) == 0) {
            return (struct bflb_device_s *)&bl616l_device_table[i];
        }
    }
    LHAL_PARAM_ASSERT(0);
    return NULL;
}

void bflb_device_set_userdata(struct bflb_device_s *device, void *user_data)
{
    device->user_data = user_data;
}